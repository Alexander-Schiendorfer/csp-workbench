    private void continueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continueButtonActionPerformed
       ProblemFactory factory = (ProblemFactory) problemFactoryList.getSelectedValue();
       Map<String, Object> parameters = new HashMap<String, Object>();

       // get property map from UI
       if(parameterUI != null)
          parameterUI.getParameters(parameters);
       
       Problem newProblem = factory.getProblem(parameters);
       subject.raiseEvent(new UICommandEvent(UICommandEvent.PROBLEM_SELECTED, newProblem));
       
    }//GEN-LAST:event_continueButtonActionPerformed

    private void problemFactoryListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_problemFactoryListValueChanged
       ProblemFactory factory = (ProblemFactory) problemFactoryList.getSelectedValue();
       continueButton.setEnabled(true);
       if(factory.getProblemParameterView() != null) {
          parameterPanel.setVisible(true);
          parametersLabel.setVisible(true);

          parameterPanel.removeAll();
          parameterPanel.add(factory.getProblemParameterView());
          parameterUI = (ProblemParameterUI) factory.getProblemParameterView();
          parameterPanel.setLayout(new GridLayout(1,1));
       } else {
          parameterPanel.removeAll();
          parametersLabel.setVisible(false);
          parameterUI = null;
       }
       updateUI();
    }//GEN-LAST:event_problemFactoryListValueChanged


