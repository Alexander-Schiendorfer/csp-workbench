    public void bind(ListenableGraph<Variable, Constraint> csp) {
        this.csp = csp;
        SwingUtilities.invokeLater(new Runnable() {

            public void run() {
                setUpChessboard();
            }
        });
    }

    @Handles(type=SearchEvent.ASSIGNMENT_OCCURED)
    public void updateAssignment(SearchEvent e) {
        SwingUtilities.invokeLater(new Runnable() {

            public void run() {
                for(Variable v : csp.vertexSet()){
                    int col = v.getAssignedValue() == null ? 0 : (Integer) v.getAssignedValue();
                    chessboard1.placeQueen(v.getId(), col);
                }
                chessboard1.repaint();
            }
        });
    }

    private void setUpChessboard() {
        chessboard1.setUp(csp.vertexSet().size());
        chessboard1.repaint();
    }
